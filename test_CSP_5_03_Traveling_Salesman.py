import CSP_5_03_Traveling_Salesman as HW

def test_full_tsp():
    my_tsp = HW.full_TSP([[30,100],[500,200],[300,300],[200,400],[350,150],[700,120],[10,10]])
    my_dist = HW.getPathDistance(my_tsp)
    print(f"my_tsp = \'{my_tsp}\', distance \'{my_dist}\'")
    test_tsp = [[30, 100], [500, 200], [700, 120], [350, 150], [300, 300], [200, 400], [10, 10]]
    test_dist = HW.getPathDistance(test_tsp)
    print(f"test_tsp = \'{test_tsp}\', test_dist = \'{test_dist}\'")
    assert HW.full_TSP([[30,100],[500,200],[300,300],[200,400],[350,150],[700,120],[10,10]]) ==[[30, 100], [500, 200], [700, 120], [350, 150], [300, 300], [200, 400], [10, 10]]
    assert HW.full_TSP([[80,75],[100,20],[530,300],[280,200],[350,150],[700,120],[10,10]]) ==[[80, 75], [100, 20], [530, 300], [700, 120], [350, 150], [280, 200], [10, 10]]


def test_heuristic_tsp():
    assert HW.heuristic_TSP([[30,100],[500,200],[300,300],[200,400],[350,150],[700,120],[10,10]]) ==[[30, 100], [10, 10], [200, 400], [300, 300], [350, 150], [500, 200], [700, 120]]
    assert HW.heuristic_TSP([[80,75],[100,20],[530,300],[280,200],[350,150],[700,120],[10,10]]) ==[[80, 75], [100, 20], [10, 10], [280, 200], [350, 150], [530, 300], [700, 120]]
